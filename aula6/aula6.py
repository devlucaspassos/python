# Aula 6 - Variáveis

# Iniciar com letras, pode conter números, separar _, letras minúsculas.

# Veja abaixo diversos tipos de dados sendo atribuídos a variáveis.
animal = 'cavalo'  # Exemplo de uma string sendo atribuída a variável "animal".
idade = 23  # Exemplo de um número inteiro (int) sendo atribuído a variável "idade".
altura = 1.90  # Exemplo de um número float sendo atribuído a variável "altura".
num = 2 > 1  # Exemplo de um valor booleano (True) sendo atribuído a variável "num".
f = False  # Exemplo de um valor booleano explícito (False) sendo atribído a variável "x".
x = 5 + 5  # Exemplo de soma sendo atribuída a variável x, neste exemplo x é igual a 10.

# Uso das variáveis

print(animal)  # >> cavalo
print(idade)  # >> 23
print(altura)  # >> 1.9
print(num)  # >> True
print(x)  # >> False

# Também é possível utilizar as variáveis para realizar operações matemáticas.
print(idade * altura)  # >> 43.699999999999996 