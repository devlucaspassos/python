# Aula 9 - Estruturas condicionais if, elif, else.

# HIERARQUIA: O bloco de códigos deve ter 4 espaços (1 TAB) em relação a palavra-chave.
if 10 > 20:
    print('banana')  # Será exibido caso a expressão da estrutura if acima for verdadeira.
elif 10 == 10:  
    print('morango') 
elif 10 == 10:  # Essa linha só será executada se as condições anteriores não forem verdadeiras.
    print('maçã')
else:
# O bloco de códigos presente abaixo só será executado caso todas as condições acima não forem verdadeiras.
    print('Não é verdadeiro')

# Caso nenhuma das condições seja verdadeira, nenhuma linha de código será executada.