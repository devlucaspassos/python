# Aula 3 - tipos de dados em Python.

# Exemplos de dados do tipo string (str).
print('morango')  # >> morango 
print("melão")  # >> melão

print("Preciso comprar uma caixa d'água")  #  Utilizando aspas diferentes para evitar erro.

print('Viajarei para Santa Bárbara d\'Oeste.')  # Utilizando o caractere de escape.

# Exemplos de dados do tipo int (números inteiros).
print(10)  # >> 10
print(-50)  # >> -50

# Exemplos de dados do tipo float (números com pontos flutantes).
print(50.12)  #  >> 50.12
print(-12.99)   #  >> -12.99








