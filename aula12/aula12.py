# Aula 12 - Função len()

lista = ['suco de goiaba', 'suco de maracujá', 'suco de acerola']

x = len(lista)  # A função len() retornará o número de itens dentro da lista.

print(x)  # >> 3 

y = 'vitamina' 

print(len(y)) # >> 8
# A função len() acima retornou o número de caracteres da string "vitamina".
